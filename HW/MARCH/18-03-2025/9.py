# Modify  following  program  such  that  every  function  should  be  executed
def  f1():
	print('No-argument  function')
	return 1
print(f1())
def  f1(x):
	print('Single  argument  function  : ' , x)
	return 1
print(f1(1))
def  f1(x , y):
	print('Two  argument  function : ' , x , y)
	return 1
print(f1(1,2))
def  f1(x , y , z):
	print('Three  argument  function : ' , x , y , z)
	return 1
print(f1(1,2,3))